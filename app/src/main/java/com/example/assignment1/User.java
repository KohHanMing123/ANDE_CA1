package com.example.assignment1;
public class User {

    private String user_id;
    private String user_name;
    private String user_email;
    private String user_pwd;
    private String class_name;
    private String phone_no;
    private String er_name;
    private String er_relationship;

    public String getUser_name() {
        return user_name;
    }

    public void setUser_name(String user_name) {
        this.user_name = user_name;
    }

    public String getUser_email() {
        return user_email;
    }

    public void setUser_email(String user_email) {
        this.user_email = user_email;
    }

    public String getUser_pwd() {
        return user_pwd;
    }

    public void setUser_pwd(String user_pwd) {
        this.user_pwd = user_pwd;
    }

    public String getClass_name() {
        return class_name;
    }

    public void setClass_name(String class_name) {
        this.class_name = class_name;
    }

    public String getPhone_no() {
        return phone_no;
    }

    public void setPhone_no(String phone_no) {
        this.phone_no = phone_no;
    }

    public String getEr_name() {
        return er_name;
    }

    public void setEr_name(String er_name) {
        this.er_name = er_name;
    }

    public String getEr_relationship() {
        return er_relationship;
    }

    public void setEr_relationship(String er_relationship) {
        this.er_relationship = er_relationship;
    }
}
